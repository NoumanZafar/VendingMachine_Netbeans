    private void jButtonRefundCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRefundCancelActionPerformed
        amountPaid = amountPaid + purchase;
        while (amountPaid > 0) {
            vendingMachine.work.money();
        }
        vendingMachine.work.refund();
        Change c = new Change();
        c.setVisible(true);
        dispose();
    }//GEN-LAST:event_jButtonRefundCancelActionPerformed

