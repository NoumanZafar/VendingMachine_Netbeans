    private void jButtonCheeseAndTomatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCheeseAndTomatoActionPerformed
        if (jRadioButtonLarge.isSelected()) {
            quantity[0][0] = vendingMachine.work.method(list1, jTextFieldTotalPurchase, name[0][0], quantity[0][0], price[0][0], 0, 0);
        } else if (jRadioButtonMedium.isSelected()) {
            quantity[1][0] = vendingMachine.work.method(list1, jTextFieldTotalPurchase, name[1][0], quantity[1][0], price[1][0], 1, 0);
        } else if (jRadioButtonSmall.isSelected()) {
            quantity[2][0] = vendingMachine.work.method(list1, jTextFieldTotalPurchase, name[2][0], quantity[2][0], price[2][0], 2, 0);
        } else {
            JOptionPane.showMessageDialog(null, "Please select Pizza Size");
        }
    }//GEN-LAST:event_jButtonCheeseAndTomatoActionPerformed

